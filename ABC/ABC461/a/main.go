package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

var sc = bufio.NewScanner(os.Stdin)

func nextInt() int {
	sc.Scan()
	i, _ := strconv.Atoi(sc.Text())
	return i
}

func main() {
	sc.Split(bufio.ScanWords)
	a, d := nextInt(), nextInt()

	if a <= d {
		fmt.Println("Yes")
	} else {
		fmt.Println("No")
	}
}
